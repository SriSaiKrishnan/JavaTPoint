package com.sai.javabasic;

public class AssignmentOperatorExample {

    public static void main(String[] args) {
        int a=10;
        a+=4;
        System.out.println(a);
        a-=3;
        System.out.println(a);
        a*=2;
        System.out.println(a);
        a/=2;
        System.out.println(a);
    }
}
